t1 DW ?
DISPLAY PROC
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	MOV BX,AX
	CMP AX,0
	JGE POS
	MOV AH,2
	MOV DL,45
	INT 21H
	MOV AX,BX 
	NEG AX
	POS:
	MOV SI,0
	WHILE_LOOP:
	MOV DX,0
	MOV BX,10
	DIV BX
	ADD DX,'0'
	MOV RESULT+SI,DL
 	ADD SI,1
 	CMP AX,0
	JNZ WHILE_LOOP
	END_LOOP:
	MOV RESULT+SI,'$'
	MOV FINAL+SI,'$'
	MOV CX,SI
	MOV DI,SI
	SUB DI,1
	MOV SI,0
	REVERSE:
	MOV BL,RESULT+DI
	MOV FINAL+SI,BL
	SUB DI,1
	ADD SI,1
	LOOP REVERSE
	MOV AH,9
	LEA DX,FINAL
	INT 21H
	MOV AH,2
	MOV DL,0DH
	INT 21H
	MOV DL,0AH
	INT 21H
	POP DX
	POP CX
	POP BX
	POP AX
	ret
